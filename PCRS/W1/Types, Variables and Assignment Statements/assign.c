#include <stdio.h>

int main() {
    // Declare a new integer variable named canada_population:
    int canada_population;

    // Assign a value to canada_population:
    canada_population = 35540000;

    // We can declare multiple variables of the
    // same type on one line, separated by commas:
    int base, height, area;

    // Set initial values:
    base = 12;
    height = 7;

    // Use an expression to calculate the value of area.
    // The right-hand side is first evaluated,
    // and then the evaluated value is assigned to
    // the variable on the left-hand side (area).
    area = base * height;

    return 0;
}